namespace InFlood.Entities.ActionSystem
{
    /// <summary>
    /// All types of Damage presented in InFlood.
    /// </summary>
    public enum DamageType
    {
        /// <summary>
        /// Damage of bullets and projectiles.
        /// </summary>
        Kinetic,
        /// <summary>
        /// All fire-related things, e.g. flamethrowers and incendiary projectiles.
        /// </summary>
        Thermal,
        /// <summary>
        /// All kinds of other energy, e.g. blasters, lasers, electomagnetic pulses.
        /// </summary>
        Energetic
    }

}